pub enum Token<'a> {
    // keywords
    Let,
    Const,
    Pub,
    Use,
    Func,
    Enum,
    Struct,

    // Literals
    String(&'a str),
    Number(f64),

    // Symbols
    Arrow,
    Assign,
    Eq,
    Gt,
    GtEq,
    Lt,
    LtEq,
    Dot,
    Question,
    At,
    Comma,
    Semicolon,
    Not,
    NotEq,
    Add,
    AddEq,
    Sub,
    SubEq,
    Mul,
    MulEq,
    Div,
    DivEq,
    Pow,
    PowEq,
    Mod,
    ModEq,
    And,
    Or,

    // Misc
    Identifier(&'a str),
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Start,
    Eof
}